		<h4>RFID SMART HEALTH CARD</h4>
		</center>
		This is an IOT project which is beneficial for a patient and doctors for recording and storing the health information digitally that can be accessed using a RFID (Radio-frequency identification) card. The name of our project is “RFID Smart Health Card”.<br>
		
        RFID SMART Health Cards are easily portable. Records can be maintained easily over a longer period of time. A complete medical history of the patient will be available. Even if the patient is not able to explain his medical condition to any doctor, it can be easily known from this card.<br>
		
        With our project, admin can register new patients by inputting their data in required data fields like name, age, DOB, gender, blood group, doctor appointed etc, and the data of the patient will be saved in the database that can be accessed with a RFID card which is unique to each patient. If doctors want to know the medical history of a patient then he/she can access it by just scanning the RFID card using a RFID card reader module.<br>
		
        This project helps in decreasing the time required to get the patient medical details and make the work of the doctors more productive and  also helps in speeding up the patient’s treatment process.<br><br>
		
